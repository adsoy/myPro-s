package day05concatinationoperatortypecasting;

public class AsciiValues {
    public static void main(String[] args) {
        /*
        Java da her karakterin sayisal numerik bir degeri vardir.
        Bu degerler ASCII degerleri olarak adlandirilir.
        Bu degerlerin tamaminin bulundugu tabloya ASCII Table denir.
         */

        char a = 'a'; //a
        System.out.println(a);


        //Herhangi bir character in ASCII degerini bulmak icin o Character i "int" data type inda variable icine koyunuz

        int intA = 'a'; //97
        System.out.println(intA);



        byte unlem = '!';
        System.out.println(unlem); //33

        char c1 = 'K';
        char c2 = '?';
        System.out.println(c1+c2); //138
        //java da char lari matematiksel islemlerde kullanirsaniz java char larin ASCII degerlerini kullanir.




































    }
}
